public class VM {

    public int m,n,o,p;

    public VM(int a, int b, int c, int d) {

        this.m=a;
        this.n=b;
        this.o=c;
        this.p=d;
        //System.out.println("VM obj created");
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    public int getO() {
        return o;
    }

    public void setO(int o) {
        this.o = o;
    }

    public int getP() {
        return p;
    }

    public void setP(int p) {
        this.p = p;
    }
}